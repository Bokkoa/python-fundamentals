# Others

numbers = [1, 2, 3, 4, 5]

# Range
# Helps to create some iterable from a specific range
# list wrapper is for list casting
numbers = list(range(1000))
print(numbers)

# join helps to glue array elements/ list elements in our string
sentence = " ".join(['Hello', 'World', 'From', 'A', 'Join', 'Xoxo'])
print(sentence)

# Sum can get a list of numbers to sum each of them
total = sum(numbers) # 1 + 2 + 3 ... + 100
print(total)

# Mazx
# Getting the highest number from the list
maximum = max(numbers)
print(maximum)

# Min
# Getting the lowest number from the list
minimum = min(numbers)
print(minimum)

# len
# Getting the list length 
elements = len(numbers)
print(elements)
