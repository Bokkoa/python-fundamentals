
# Equality ==
print(5 == 5) # True
print(5 == 3) # False
print("Hello" == "Hello") # True

# Not equal !=
print(5 != 5) # False
print(5 != 3) # True
print("Hello" == "Hello") # False


# More than >
print(7 > 5) # True
print(5 > 9) # False

# Less than <
print(7 < 5) # False
print(5 < 9) # True

# More or equal than >
print(7 >= 5) # True
print(5 >= 5) # True
print(5 >= 9) # False

# Less or equal than <
print(7 <= 5) # False
print(5 <= 9) # True
print(5 <= 5) # True